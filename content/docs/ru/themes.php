<p>Темы определяют внешний вид сайта: цвета, шрифты, шаблоны страниц.</p>

<h3>Выбор темы</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/themes') ?>">Темы</a>.</li>
    <li>Выберите активную тему и нажмите «Активировать».</li>
    <li>Или смените тему в <a href="<?= url('admin/settings') ?>">Настройках</a>.</li>
</ol>

<h3>Визуальный настройщик</h3>
<ol>
    <li>В разделе «Темы» нажмите «Настроить оформление» у активной темы.</li>
    <li>Измените цвета, шрифты и скругления углов с предпросмотром.</li>
    <li>Сохраните изменения — они применятся на всём сайте.</li>
    <li>При необходимости сбросьте настройки к значениям по умолчанию.</li>
</ol>

<h3>Редактор файлов темы</h3>
<p>Для опытных пользователей доступно редактирование PHP-шаблонов, SCSS и CSS. Используйте только если визуального настройщика недостаточно.</p>
<ol>
    <li>Нажмите «Редактировать файлы» у темы.</li>
    <li>Выберите файл в списке слева.</li>
    <li>Внесите изменения и сохраните.</li>
</ol>

<div class="help-warning">
    <strong>Важно:</strong> перед правкой файлов темы сделайте резервную копию. Ошибки в шаблонах могут нарушить отображение сайта. При обновлении CMS файлы в <code>content/themes/</code> сохраняются.
</div>

<div class="help-tip">
    <strong>Совет:</strong> для смены логотипа достаточно <a href="<?= url('admin/help/settings') ?>">настроек сайта</a> — редактировать тему не обязательно.
</div>
