<p>Модуль «Расписание» показывает расписание занятий по классам на странице <code>/schedule</code>.</p>

<h3>Настройка расписания</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/schedule') ?>">Расписание</a>.</li>
    <li>Добавьте классы (например, «5А», «10Б»).</li>
    <li>Выберите класс и заполните расписание по дням недели.</li>
    <li>Для каждого урока укажите время, предмет и при необходимости кабинет.</li>
    <li>Сохраните изменения.</li>
</ol>

<h3>Обновление</h3>
<p>При смене четверти или учебного года обновите расписание для всех классов. Старое расписание заменяется новым.</p>

<div class="help-tip">
    <strong>Совет:</strong> сначала создайте все классы, затем заполняйте расписание по одному — так проще контролировать полноту данных.
</div>
