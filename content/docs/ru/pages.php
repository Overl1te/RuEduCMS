<p>Страницы — основной способ размещения статичного контента: информация о школе, правила, объявления в виде отдельных разделов.</p>

<h3>Создание страницы</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/pages') ?>">Страницы</a> → «Создать».</li>
    <li>Введите <strong>заголовок</strong> страницы.</li>
    <li>Задайте <strong>URL (slug)</strong> — короткое имя латиницей (например, <code>priem-v-shkolu</code>). Страница будет доступна по адресу <code>/page/slug</code>.</li>
    <li>Напишите текст в визуальном редакторе.</li>
    <li>Выберите статус: <strong>Черновик</strong> (не видна на сайте) или <strong>Опубликовано</strong>.</li>
    <li>Заполните SEO-поля при необходимости и нажмите «Сохранить».</li>
</ol>

<h3>Системные и пользовательские страницы</h3>
<p>В списке страниц есть два типа:</p>
<ul>
    <li><strong>Системные</strong> — главная, новости, сведения об ОО и др. Управляются модулями и шаблонами темы. Редактирование контента — через соответствующий раздел админки.</li>
    <li><strong>Пользовательские</strong> — созданные вами страницы в разделе «Страницы».</li>
</ul>

<h3>SEO-поля</h3>
<ul>
    <li><strong>Meta title</strong> — заголовок для поисковиков (если пусто — используется заголовок страницы).</li>
    <li><strong>Meta description</strong> — краткое описание для сниппета в поиске.</li>
</ul>

<div class="help-tip">
    <strong>Совет:</strong> для новостей и объявлений с датой используйте раздел <a href="<?= url('admin/help/articles') ?>">Новости</a>, а не отдельные страницы.
</div>

<div class="help-warning">
    <strong>Ограничение:</strong> авторы могут создавать и редактировать страницы, но публиковать и удалять — только редакторы и администраторы.
</div>
