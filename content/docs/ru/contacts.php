<p>Контакты и формы обратной связи помогают посетителям связаться с учреждением и оставить заявку.</p>

<h3>Страница контактов</h3>
<ol>
    <li>Укажите телефон и адрес в <a href="<?= url('admin/help/settings') ?>">настройках сайта</a>.</li>
    <li>Добавьте карту Яндекса в настройках (см. раздел «Яндекс.Карта»).</li>
    <li>Страница доступна по адресу <code>/contacts</code>.</li>
</ol>

<h3>Формы обратной связи</h3>
<ol>
    <li>Формы встраиваются на страницу контактов автоматически при включённом модуле.</li>
    <li>Текст согласия на обработку персональных данных настраивается в <a href="<?= url('admin/settings') ?>">Настройках</a> (ФЗ-152).</li>
    <li>Заявки поступают в раздел <a href="<?= url('admin/forms/submissions') ?>">Заявки</a> в боковом меню.</li>
</ol>

<h3>Просмотр заявок</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/forms/submissions') ?>">Заявки</a>.</li>
    <li>Просмотрите список входящих сообщений.</li>
    <li>Отметьте прочитанные заявки.</li>
</ol>

<p>На email администратора (из настроек) также могут приходить уведомления о новых заявках.</p>

<div class="help-warning">
    <strong>Важно:</strong> не публикуйте персональные данные заявителей на сайте. Заявки доступны только в админ-панели.
</div>
