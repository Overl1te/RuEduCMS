<p>Модуль «Галерея» создаёт фотоальбомы для мероприятий, жизни школы и других событий.</p>

<h3>Создание альбома</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/gallery') ?>">Галерея</a>.</li>
    <li>Нажмите «Создать альбом».</li>
    <li>Укажите название и описание альбома.</li>
    <li>Загрузите фотографии в альбом.</li>
    <li>Сохраните.</li>
</ol>

<h3>Просмотр на сайте</h3>
<p>Альбомы доступны на <code>/gallery</code>. Каждый альбом имеет отдельную страницу с сеткой фотографий.</p>

<div class="help-tip">
    <strong>Совет:</strong> перед загрузкой уменьшите размер фотографий (до 1920 px по длинной стороне) — сайт будет загружаться быстрее.
</div>
