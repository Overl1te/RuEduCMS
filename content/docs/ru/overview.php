<p>RuEduCMS — система управления сайтом образовательного учреждения. Эта справка поможет настроить сайт и ежедневно работать с контентом.</p>

<h3>Первый вход</h3>
<ol>
    <li>После установки откройте админ-панель: <a href="<?= url('admin/') ?>">Панель управления</a>.</li>
    <li>Войдите с логином и паролем, созданными при установке.</li>
    <li>Если появится мастер настройки — пройдите его: укажите данные организации и выберите нужные модули.</li>
</ol>

<h3>Мастер настройки</h3>
<p>Мастер запускается при первом входе администратора. Он помогает быстро заполнить основные данные:</p>
<ul>
    <li>название и описание сайта;</li>
    <li>контактные данные;</li>
    <li>включение модулей (новости, сведения об ОО, расписание и др.);</li>
    <li>базовые сведения об организации.</li>
</ul>
<p>Мастер можно пройти позже через рекомендации на <a href="<?= url('admin/') ?>">главной странице админки</a>.</p>

<h3>Разделы бокового меню</h3>
<ul>
    <li><strong>Панель управления</strong> — статистика и быстрые действия.</li>
    <li><strong>Страницы, Новости, Медиа, Меню</strong> — основной контент сайта.</li>
    <li><strong>Пользователи, Модули, Темы, Обновления, Настройки</strong> — только для администратора.</li>
    <li><strong>Справка</strong> — эта документация.</li>
</ul>
<p>Дополнительные пункты (Сведения об ОО, Педагоги, Расписание и др.) появляются при включении соответствующих модулей.</p>

<h3>Роли пользователей</h3>
<p>Права зависят от роли. Подробнее — в разделе <a href="<?= url('admin/help/users') ?>">Пользователи и роли</a>.</p>

