<h3>Добавление пользователя</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/users') ?>">Пользователи</a> (только администратор).</li>
    <li>Нажмите «Создать».</li>
    <li>Укажите имя, email, логин и пароль.</li>
    <li>Выберите роль.</li>
    <li>Сохраните.</li>
</ol>

<h3>Безопасность</h3>
<ul>
    <li>Используйте надёжные пароли (не менее 8 символов).</li>
    <li>Назначайте роль «Администратор» только доверенным сотрудникам.</li>
    <li>Для учителей, публикующих новости, достаточно роли «Редактор» или «Автор».</li>
</ul>

<div class="help-tip">
    <strong>Совет:</strong> справка доступна всем авторизованным пользователям — авторы могут изучить, как создавать материалы, даже без доступа к настройкам.
</div>
