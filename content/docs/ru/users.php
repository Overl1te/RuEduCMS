<p>Управление учётными записями людей, которые работают с админ-панелью.</p>

<h3>Роли</h3>
<table class="table table-sm">
    <thead>
        <tr><th>Роль</th><th>Возможности</th></tr>
    </thead>
    <tbody>
        <tr>
            <td><strong>Администратор</strong></td>
            <td>Полный доступ: настройки, пользователи, модули, темы, обновления, весь контент</td>
        </tr>
        <tr>
            <td><strong>Редактор</strong></td>
