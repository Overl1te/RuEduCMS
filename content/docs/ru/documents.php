<p>Модуль «Документы» позволяет размещать файлы для скачивания: устав, локальные акты, отчёты и другие материалы.</p>

<h3>Загрузка документа</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/documents') ?>">Документы</a>.</li>
    <li>Нажмите «Добавить документ».</li>
    <li>Укажите название и выберите категорию (устав, локальные акты, отчёты и др.).</li>
    <li>Загрузите файл (PDF, DOC, DOCX и др.).</li>
    <li>Сохраните.</li>
</ol>

<h3>Категории</h3>
<p>Документы группируются по категориям на публичной странице <code>/documents</code>. Используйте понятные названия, чтобы посетители быстро находили нужный файл.</p>

<div class="help-tip">
    <strong>Совет:</strong> для обязательных документов в разделе «Сведения об ОО» загрузите файлы здесь и укажите ссылки в соответствующем разделе <a href="<?= url('admin/help/sveden') ?>">Сведений</a>.
</div>
