<p>Медиабиблиотека хранит изображения и файлы, загруженные для использования на сайте.</p>

<h3>Загрузка файлов</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/media') ?>">Медиа</a>.</li>
    <li>Нажмите «Загрузить» и выберите файлы с компьютера.</li>
    <li>После загрузки файл появится в списке с превью (для изображений).</li>
</ol>

<h3>Использование в контенте</h3>
<ol>
    <li>При редактировании страницы или новости вставьте изображение через редактор (кнопка «Изображение»).</li>
    <li>Выберите файл из медиабиблиотеки или загрузите новый.</li>
    <li>Для вставки ссылки на файл скопируйте URL из медиабиблиотеки.</li>
</ol>

<h3>Удаление</h3>
<p>Удалять файлы могут редакторы и администраторы. Убедитесь, что файл не используется на опубликованных страницах.</p>

<div class="help-tip">
    <strong>Совет:</strong> для документов учреждения (PDF, Word) удобнее использовать модуль <a href="<?= url('admin/help/documents') ?>">Документы</a> — он группирует файлы по категориям.
</div>
