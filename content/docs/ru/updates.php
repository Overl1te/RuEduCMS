<p>Раздел «Обновления» позволяет проверять новые версии RuEduCMS, обновлять базу данных и применять обновления системы.</p>

<h3>Проверка обновлений</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/updates') ?>">Обновления</a> (только администратор).</li>
    <li>Система покажет текущую версию и наличие новой версии.</li>
    <li>Если доступно обновление — следуйте инструкциям на странице.</li>
</ol>

<h3>Миграции базы данных</h3>
<p>При обновлении CMS может потребоваться обновление структуры БД. На панели управления и в разделе «Обновления» появится предупреждение — нажмите «Обновить базу данных».</p>

<h3>Резервное копирование</h3>
<ol>
    <li>Перед обновлением сделайте резервную копию файлов и базы данных на хостинге.</li>
    <li>В разделе «Обновления» доступна функция создания резервной копии (если поддерживается хостингом).</li>
</ol>

<h3>Что сохраняется при обновлении</h3>
<ul>
    <li>Папка <code>content/</code> — темы, модули, загрузки, справка</li>
    <li>Файл <code>config.php</code></li>
    <li>Данные в базе данных</li>
</ul>
<p>Обновляются только системные папки <code>core/</code> и <code>admin/</code>.</p>

<div class="help-warning">
    <strong>Важно:</strong> не редактируйте файлы в папке <code>core/</code> — изменения будут потеряны при следующем обновлении.
</div>
