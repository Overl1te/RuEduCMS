<p>Меню управляет навигацией сайта: ссылками в шапке и боковой панели.</p>

<h3>Редактирование меню</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/menus') ?>">Меню</a>.</li>
    <li>Выберите меню: <strong>Главное</strong> (шапка сайта) или <strong>Боковое</strong> (сайдбар на внутренних страницах).</li>
    <li>Добавьте пункты: укажите название и URL.</li>
    <li>Измените порядок перетаскиванием или кнопками вверх/вниз.</li>
    <li>Нажмите «Сохранить».</li>
</ol>

<h3>Формат URL</h3>
<ul>
    <li>Внутренние страницы: <code>/page/informaciya</code>, <code>/news</code>, <code>/sveden</code></li>
    <li>Внешние ссылки: полный адрес с <code>https://</code></li>
    <li>Главная страница: <code>/</code></li>
</ul>

<div class="help-tip">
    <strong>Совет:</strong> при установке CMS меню заполняется типовой структурой. Отключите ненужные разделы в <a href="<?= url('admin/help/modules') ?>">Модулях</a>, затем уберите лишние пункты из меню.
</div>

<div class="help-warning">
    <strong>Важно:</strong> редактирование меню доступно редакторам и администраторам.
</div>
