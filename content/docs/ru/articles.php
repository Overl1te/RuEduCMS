<p>Раздел «Новости» предназначен для публикации объявлений, событий и пресс-релизов с датой. Лента доступна на сайте по адресу <code>/news</code>, также генерируется RSS-лента.</p>

<h3>Публикация новости</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/articles') ?>">Новости</a> → «Создать».</li>
    <li>Введите заголовок и краткий анонс (отображается в списке новостей).</li>
    <li>Напишите полный текст в редакторе.</li>
    <li>Выберите категорию при необходимости.</li>
    <li>Укажите дату публикации.</li>
    <li>Установите статус «Опубликовано» и сохраните.</li>
</ol>

<h3>Категории</h3>
<p>Категории помогают группировать новости (например, «Объявления», «Мероприятия»). Создавайте их при добавлении или редактировании статьи.</p>

<h3>RSS</h3>
<p>После публикации новостей подписчики могут использовать RSS-ленту по адресу <code>/news/rss</code>.</p>

<div class="help-tip">
    <strong>Совет:</strong> для долгосрочной информации (устав, правила приёма) лучше создать <a href="<?= url('admin/help/pages') ?>">страницу</a>, а не новость.
</div>
