<p>Модули — функциональные блоки сайта: новости, сведения об ОО, расписание и другие разделы, требуемые для сайта образовательного учреждения.</p>

<h3>Включение и отключение</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/modules') ?>">Модули</a> (только администратор).</li>
    <li>Используйте переключатель рядом с модулем.</li>
    <li>Отключённый модуль скрывает соответствующий раздел на сайте и пункт в админ-меню.</li>
</ol>

<h3>Разделы (страницы)</h3>
<p>Ниже списка модулей отображаются разделы — отдельные страницы типовой структуры сайта (приём в школу, безопасность, ГИА и др.). Их тоже можно включать и отключать независимо.</p>

<h3>Доступные модули</h3>
<ul>
    <li><strong>Сведения об ОО</strong> — <a href="<?= url('admin/help/sveden') ?>">обязательный раздел</a> для сайтов ОО</li>
    <li><strong>Новости</strong> — лента объявлений</li>
    <li><strong>Педагоги</strong> — состав сотрудников</li>
    <li><strong>Расписание</strong> — расписание занятий</li>
    <li><strong>Документы</strong> — файлы для скачивания</li>
    <li><strong>Галерея</strong> — фотоальбомы</li>
    <li><strong>Контакты</strong> — страница контактов</li>
    <li><strong>Формы</strong> — обратная связь и заявки</li>
</ul>

