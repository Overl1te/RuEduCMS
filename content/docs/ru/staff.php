<p>Модуль «Педагоги» размещает информацию о сотрудниках учреждения на странице <code>/staff</code>.</p>

<h3>Добавление сотрудника</h3>
<ol>
    <li>Откройте <a href="<?= url('admin/staff') ?>">Педагоги</a>.</li>
    <li>Нажмите «Добавить».</li>
    <li>Укажите ФИО, должность, предмет (для учителей), стаж, образование.</li>
    <li>Загрузите фотографию (рекомендуется портретное фото).</li>
    <li>Добавьте контакты: email, телефон при необходимости.</li>
    <li>Сохраните запись.</li>
</ol>

<h3>Порядок отображения</h3>
<p>Сотрудники отображаются в порядке, заданном при создании. При необходимости измените порядок сортировки в списке.</p>

